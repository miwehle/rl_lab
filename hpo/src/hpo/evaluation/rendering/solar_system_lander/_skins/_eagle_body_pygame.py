def _rgb(hex_color):
    if hex_color is None or hex_color == "none":
        return None
    hex_color = hex_color.lstrip("#")
    return tuple(int(hex_color[i : i + 2], 16) for i in (0, 2, 4))


def _transform_points(points, x, y, scale):
    return [(int(round(x + px * scale)), int(round(y + py * scale))) for px, py in points]


def draw_eagle_body(surface, x=0, y=0, scale=1.0, scale_strokes=True):
    for kind, fill_hex, stroke_hex, stroke_width, points in EAGLE_BODY_OPS:
        pts = _transform_points(points, x, y, scale)
        fill = _rgb(fill_hex)
        stroke = _rgb(stroke_hex)
        width = (
            max(1, int(round(stroke_width * scale))) if scale_strokes else max(1, int(round(stroke_width)))
        )

        if kind == "polygon":
            if fill is not None:
                pygame.draw.polygon(surface, fill, pts, 0)
            if stroke is not None and len(pts) >= 2:
                pygame.draw.polygon(surface, stroke, pts, width)
        elif kind == "polyline":
            color = stroke or fill
            if color is not None and len(pts) >= 2:
                pygame.draw.lines(surface, color, False, pts, width)


def make_eagle_body_surface(scale=1.0, background=None):
    w = int(round(SVG_WIDTH * scale))
    h = int(round(SVG_HEIGHT * scale))
    surface = pygame.Surface((w, h), pygame.SRCALPHA)
    if background is not None:
        surface.fill(background)
    draw_eagle_body(surface, 0, 0, scale)
    return surface


def save_eagle_body_png(filename="eagle_body.png", scale=1.0, background=(255, 255, 255)):
    pygame.init()
    surface = make_eagle_body_surface(scale=scale, background=background)
    pygame.image.save(surface, filename)
    return filename
