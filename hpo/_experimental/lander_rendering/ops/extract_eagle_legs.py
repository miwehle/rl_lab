"""Extract landing-leg drawing ops from the detailed Eagle Pygame module.

This is a design helper. It keeps the static Eagle leg graphics as a reference
for building later Gymnasium-driven moving legs.
"""

from __future__ import annotations

from collections.abc import Iterable, Sequence
from pathlib import Path
import sys
from typing import TypeAlias

Point: TypeAlias = tuple[float, float]
Op: TypeAlias = tuple[str, str | None, str | None, float, list[Point]]

GRAPHICS_DIR = Path(__file__).resolve().parents[1]
DETAILED_DIR = GRAPHICS_DIR / "detailed"
DEFAULT_OUTPUT = DETAILED_DIR / "eagle_legs_pygame.py"


def extract_leg_ops(ops: Iterable[Op]) -> list[Op]:
    """Return the outer left and right landing-leg ops from the Eagle drawing."""

    return [
        (kind, fill, stroke, stroke_width, list(points))
        for kind, fill, stroke, stroke_width, points in ops
        if _is_outer_leg(points)
    ]


