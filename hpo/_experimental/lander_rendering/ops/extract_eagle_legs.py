def _is_outer_leg(points: Sequence[Point]) -> bool:
    x1, y1, x2, y2 = _bbox(points)
    width = x2 - x1
    height = y2 - y1

    is_left = x2 <= 150
    is_right = x1 >= 340
    is_lower = y2 >= 220
    is_leg_like = height >= 6 or width >= 10
    return (is_left or is_right) and is_lower and is_leg_like


def _bbox(points: Sequence[Point]) -> tuple[float, float, float, float]:
    xs = [point[0] for point in points]
    ys = [point[1] for point in points]
    return min(xs), min(ys), max(xs), max(ys)


def _load_eagle_ops() -> list[Op]:
    sys.path.insert(0, str(DETAILED_DIR))
    from eagle_lander_pygame import EAGLE_OPS

    return [(kind, fill, stroke, stroke_width, list(points)) for kind, fill, stroke, stroke_width, points in EAGLE_OPS]


def main() -> int:
    ops = extract_leg_ops(_load_eagle_ops())
    write_pygame_module(DEFAULT_OUTPUT, ops)
    print(f"leg ops: {len(ops)}")
    print(f"wrote: {DEFAULT_OUTPUT}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
